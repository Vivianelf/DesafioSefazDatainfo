CREATE TABLE usuario (
   email VARCHAR(50) NOT NULL,
   nome VARCHAR(100) NOT NULL,
   senha VARCHAR(10) NOT NULL,
   PRIMARY KEY (email) 
);

CREATE TABLE telefone (
   id INT NOT NULL,
   ddd INT NOT NULL,
   numero VARCHAR(12) NOT NULL,
   tipo VARCHAR(10) NOT NULL,
   email_usuario VARCHAR(50) NOT NULL
);

